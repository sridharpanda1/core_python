#WAP to take single digit from keyboard and print its value in Engilsh
n=int(input('Enter a Single digit number: '))
if n == 1:
	print('ONE')
elif n == 2:
	print('TWO')
elif n == 3:
	print('THREE')
elif n == 4:
	print('FOUR')
elif n == 5:
	print('FIVE')
elif n == 6:
	print('SIX')
elif n == 7:
	print('SEVEN')
elif n == 8:
	print('EIGHT')
elif n == 9:
	print('NINE')
elif n == 0:
	print('ZERO')
else:
	print('Not a Single Digit Number')
